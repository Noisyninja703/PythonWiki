#lists can be modifed []

# init
list = ['One' , 'Two' , 'Three' , 'Four' , 'Five' , 'Six' , 'Seven' , 'Eight' , 'Nine' , 'Ten']

# print literal
print(list)
print(list[0]) # print at 1st index
print(list[-1]) # print at last index
print() # leave line

#access
print(list[1:]) # print from index 1 -> ...
print(list[-2:]) # print from -1 -> -2
print(list[2:5]) # print from 2 -> before 5
print() # leave line

#change
list[1] = "2"
print(list)

